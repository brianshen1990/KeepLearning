/**
 * @param {number[]} count
 * @return {number[]}
 */
var sampleStats = function(count) {
    
    // min 
    let min = -1;
    for ( let i = 0 ; i < count.length ; i++ ) {
        if ( count[i] > 0 ) {
            min = i;
            break;
        }
    } 
    
    // max
    let max = -1;
    for ( let i = count.length-1 ; i >= 0 ; i-- ) {
        if ( count[i] > 0 ) {
            max = i;
            break;
        }
    }      
    
    // mode
    let mode = -1;
    let maxMode = Math.max( ...count );
    for ( let i = 0 ; i < count.length ; i++ ) {
        if ( count[i] === maxMode ) {
            mode = i;
            break;
        }
    }
    
    // mean
    let sum = count.reduce( (prev, cur, index) => prev + cur * index, 0 );
    let countNumber = count.reduce( (prev, cur) => cur+prev, 0 );
    let mean = sum / countNumber;
    
    // medium
    let medium = 0;
    let medCount = [];
    
    if ( countNumber % 2 === 0 ) {
        medCount = [countNumber/2, countNumber/2+1];
        // console.log("here", medCount )
        let countNow = 0;
        for ( let i = 0 ; i < count.length; i++ ) {
            // console.log( count[i] > 0, countNow, countNow + count[i], medCount )
            if ( count[i] > 0 && countNow < medCount[0] && 
                    ( countNow + count[i] >= medCount[0] ) ) {
                medium += i;
                // console.log("here even first", i)
            } 
            if ( count[i] > 0 && countNow < medCount[1] && 
                    ( countNow + count[i] >= medCount[1] ) ) {
                medium += i;
                // console.log("here even second", i)
                break;
            } 
            countNow += count[i];
        }
        medium = medium/2;
    } else {
        medCount = [(countNumber+1)/2];
        // console.log("here", medCount )
        let countNow = 0;
        for ( let i = 0 ; i < count.length; i++ ) {
            if ( count[i] >0 && countNow < medCount[0] && 
                    countNow + count[i] >= medCount[0] ) {
                medium = i;
                // console.log("here odd", i)
                break;
            }
            countNow += count[i];
        }
    }
    

    return [min, max, mean, medium, mode]
    
};


